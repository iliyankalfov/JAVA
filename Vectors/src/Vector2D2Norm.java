/*
 * A class which creates a two dimensional vector
 */
public class Vector2D2Norm extends Vector3D2Norm {

	/*
	 * A constructor initialising a two-dimensional vector by calling the
	 * constructor of the superclass and setting the z coordinate to 0
	 */
	public Vector2D2Norm(double x, double y) {
		super(x, y, 0);
	}

}
